package LinkedList;

public class LinkedNode {
	
	// instance variables
	int value;
	// reference to another node
	LinkedNode next;
	
	// constructors
	public LinkedNode (int value) {
		this.value = value;
	}
	
	public LinkedNode (int value, LinkedNode next) {
		this.value = value;
		this.next = next;
	}
	// methods (0  or more)

}
